let weekno=4;

switch(weekno)
{
    case 1:
         console.log("sunday");
         break;
    case 2:
         console.log("monday");
         break;
    case 3: 
    console.log("wednesday");
    break;
    case 4: 
    console.log("thursday");
    break;
    default: 
    console.log("invalid week number");
}